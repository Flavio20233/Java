package Escola;


public class Aluno {
    private String nome;
    private int  idade;
    private int Nota;
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    public int getIdade() {
        return idade;
    }
    public void setIdade(int idade) {
        this.idade = idade;
    }
    public int getNota() {
        return Nota;
    }
    public void setNota(int nota) {
        Nota = nota;
    }
    

}
